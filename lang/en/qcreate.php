<h1>Graded Question Types</h1>

<p>Here you specify the type of questions that need to be made. If you select "Allow all question types", then students can make any type of question up to the total specified in "Total Questions Graded".</p>

<p>If your students already know how to make questions, and they know the differences between the question types, it is safe to select "Allow all question types". However, if they are new to question creation, it is probably best to specify here the type of questions you want students to create.</p>

<h2>Example 1:</h2>
<p>If you select '4' in "Total Questions Graded", and check "Allow all question types" in "Graded Question Types", students will be able to create any type of questions; they could be 4 of the same type or a combination of question types. Note, the "Will Grade Minimum Number of Questions of Type" menus do not need to be set at all.</p>

<h2>Example 2:</h2>
<p>If you select '4' in "Total Questions Graded", and in "Graded Question Types" you check "Multiple Choice", students will have to create 4 Multiple choice questions. Again, the "Will Grade Minimum Number of Questions of Type" menus do not need to be set at all.</p>

<h2>Example 3:</h2>
<p>If you select '4' in "Total Questions Graded", and check "Matching" and "Multiple Choice" in "Graded Question Types", students will have to make a total of 4 questions; they could be all Matching, all Multiple Choice, or a combination of the two.</p>

<p><em>Note, if you want to to specify exactly what type of questions and how many of each type students should create, you need to indicate these parameters in the "Will Grade Minimum Number of Questions of Type" menus.
