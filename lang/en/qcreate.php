<h1>Ratio of Automatic / Manual Grading</h1>

<p>Here you specify how the total Grade will be divided: the percentage for the Automatic grade is on the left, and the Manual grade is on the right.</p>

<p>The automatic grade is the grade 'given' by the system for simply making a question. So, for example if you require students to make 10 questions, and you assign 100% for Automatic grading, a student that makes 7 questions will get 70% for the activity. On the other hand, if you asign 50% for Automatic grading, a student that creates 7 questions will get 35% for the activity (7/10 x 50% = 35%).</p>

<p>Note that the automatic grade is no guarantee of quality. The Automatic grade is best used in the following situations:</p>

<p>1) ...when you want to reward students for simply making the correct number of corrections. In  this case, it is probably best to keep the weight for the automatic grade low.</p>

<p>2) ...when you are confident that the quality of the questioins your students produce is not in doubt. For example, your students if your students are both experienced at creating questions with Moodle, and experienced at writing questions in general then a high weight or even 100% for the autimatic grade may be appropriate.</p>


<p>The Manual grade is the grade that you give for each question. So, if you asign 50% for Automatic grading, the other half of the grade will be your manually assigned grade. So, if a student creates 7 questions, they will get 35% Automatic grade, and if you grade each question as perfect they will get a total of 70% (35% automatic + 35% manual). On the other hand, if the questions have problems and you only give 60% for each question of the 7 questions, the student's total grade will be 56%. Note, it is possible to give different grades to each question. The calculation is as follows:</p>

<p>Automatic grade = 7/10 x 50% = 35%</p>

<p>Manual grade = (60% + 60% +60% + 60% + 60% + 60% + 60%)/7 x 35% = 21%</p>

<p>Total = 35% + 21% = 56%</p>

